# =====================================================================
# Outer-ring model (discrete nested-torus outer ring, L6)
# =====================================================================

N_RING = 8  # outer-ring sites; coarse discrete stand-in, finite carrier

def outer_ring_loop(direction):
    """
    direction in {+1, -1}: CW or CCW traversal of outer ring.
    Returns oriented step sequence (list of +/-1 per step).
    Closed loop of length N_RING.
    """
    assert direction in (+1, -1)
    return [direction] * N_RING

def chi(steps):
    """Signed outer-ring traversal invariant. sum of oriented step deltas."""
    return int(np.sign(sum(steps))) if sum(steps) != 0 else 0

def chiral_density_bookkeeping(direction):
    """
    L8 stand-in: Type 1 loads rho_L, Type 2 loads rho_R. We just return
    a 2-vector (rho_L, rho_R) with unit mass on one side per direction.
    """
    if direction == +1:
        return (1.0, 0.0)
    else:
        return (0.0, 1.0)

def classify(steps):
    c = chi(steps)
    if c == +1:
        return "Type1"
    if c == -1:
        return "Type2"
    return "UNASSIGNED"


