    results["N3_no_chirality_collapse"] = {
        "type1": flat_t1,
        "type2": flat_t2,
        "pass": flat_t1 == flat_t2,  # collapse by design
    }

    # N4: Random non-oriented loops should land in UNASSIGNED or single
    # class, but the ensemble must NOT yield both Type1 and Type2 from
