# TEST 2: Axis 3×6 Disentanglement — Better math formulations
# ═══════════════════════════════════════════════════════════════════

def test2_ax3_ax6_disentangle():
    """
    Problem: Axis 3 (±G) and Axis 6 (Aρ/ρA) both produce commutator
    structure, leading to 0.71 overlap.
    
    Hypothesis: The overlap comes from bad math choices, not real redundancy.
    
    Test 3 alternative formulations for Axis 3:
      (A) ±G via generator sign (current — conflates)
      (B) ±G via conjugate representation: U vs U* (Weyl-grounded)
      (C) ±G via Hopf fiber orientation: e^{+iθ} vs e^{-iθ}
    
    And 2 for Axis 6:
      (X) Aρ vs ρA (current — conflates)
      (Y) O(ρ) vs O†(ρ) (adjoint action, no commutator)
    
    Test all 3×2 = 6 combinations. Pick the one with lowest overlap.
    """
    print("\n" + "=" * 70)
    print("TEST 2: Axis 3×6 Disentanglement — Finding Better Math")
    print("=" * 70)
    
    d = 8
    n_trials = 200
    
    def ax3_A(rho, d_val):
        """Current: ±G sign flip → 2G(ρ)dt"""
        H = np.random.randn(d_val, d_val) + 1j * np.random.randn(d_val, d_val)
        H = (H + H.conj().T) / 2
        L = np.zeros((d_val, d_val), dtype=complex)
        for k in range(d_val - 1):
            L[k, k+1] = 0.3
        ham = -1j * (H @ rho - rho @ H)
        lind = L @ rho @ L.conj().T - 0.5 * (L.conj().T @ L @ rho + rho @ L.conj().T @ L)
        G = ham + lind
        dt = 0.2
        r_plus = rho + G * dt
        r_minus = rho - G * dt
        for r in [r_plus, r_minus]:
            r[:] = (r + r.conj().T) / 2
            ev, evx = np.linalg.eigh(r)
            ev = np.maximum(ev, 0)
            r[:] = evx @ np.diag(ev) @ evx.conj().T
            r /= np.trace(r)
        return r_plus - r_minus
    
    def ax3_B(rho, d_val):
        """Conjugate representation: UρU† vs U*ρU†*"""
        H = np.random.randn(d_val, d_val) + 1j * np.random.randn(d_val, d_val)
        H = (H + H.conj().T) / 2
        U = la.expm(-1j * H * 0.3)
        rho_L = U @ rho @ U.conj().T              # left Weyl: U
        rho_R = np.conj(U) @ rho @ np.conj(U).T   # right Weyl: U*
        return rho_L - rho_R
    
    def ax3_C(rho, d_val):
        """Hopf fiber: e^{+iθ} vs e^{-iθ} phase rotation"""
        theta = 0.4
        phase_plus = np.exp(1j * theta) * np.eye(d_val)
        phase_minus = np.exp(-1j * theta) * np.eye(d_val)
        rho_plus = phase_plus @ rho @ phase_plus.conj().T
        rho_minus = phase_minus @ rho @ phase_minus.conj().T
        return rho_plus - rho_minus
    
    def ax6_X(rho, d_val):
        """Current: Aρ − ρA (commutator)"""
        A = np.random.randn(d_val, d_val) + 1j * np.random.randn(d_val, d_val)
        A /= np.linalg.norm(A)
        return A @ rho - rho @ A
    
    def ax6_Y(rho, d_val):
        """Adjoint action: O(ρ) vs O†(ρ)"""
        H = np.random.randn(d_val, d_val) + 1j * np.random.randn(d_val, d_val)
        H = (H + H.conj().T) / 2
        U = la.expm(-1j * H * 0.3)
        # O(ρ) = UρU†
        rho_O = U @ rho @ U.conj().T
        # O†(ρ) = U†ρU
        rho_Odag = U.conj().T @ rho @ U
        return rho_O - rho_Odag
    
    ax3_options = [
        ("A: ±G sign", ax3_A),
        ("B: U vs U* (Weyl)", ax3_B),
        ("C: e^{±iθ} (fiber)", ax3_C),
    ]
    ax6_options = [
        ("X: Aρ − ρA (commutator)", ax6_X),
        ("Y: O vs O† (adjoint)", ax6_Y),
    ]
    
    print(f"\n  Testing {len(ax3_options)}×{len(ax6_options)} = {len(ax3_options)*len(ax6_options)} combinations at d={d}:")
    print(f"  {'':30s} {'X: [A,ρ]':>15s}  {'Y: O vs O†':>15s}")
    
    results = {}
    best_pair = None
    best_overlap = 1.0
    
    for ax3_name, ax3_fn in ax3_options:
        row = f"  {ax3_name:30s}"
        for ax6_name, ax6_fn in ax6_options:
            total = 0.0
            for trial in range(n_trials):
                np.random.seed(trial + 8000)
                rho = make_random_density_matrix(d)
                np.random.seed(trial + 8000)
                d3 = ax3_fn(rho, d)
                np.random.seed(trial + 8000)
                d6 = ax6_fn(rho, d)
                total += normalized_overlap(d3, d6)
            avg = total / n_trials
            results[f"{ax3_name} × {ax6_name}"] = avg
            marker = "✅" if avg < 0.1 else ("⚠️" if avg < 0.3 else "❌")
            row += f"  {avg:8.4f} {marker}"
            if avg < best_overlap:
                best_overlap = avg
                best_pair = (ax3_name, ax6_name)
        print(row)
    
    print(f"\n  BEST PAIR: {best_pair[0]} × {best_pair[1]} → overlap = {best_overlap:.4f}")
    if best_overlap < 0.1:
        print(f"  ✅ ORTHOGONAL: These formulations make Ax3 and Ax6 genuinely distinct.")
    elif best_overlap < 0.3:
        print(f"  ⚠️  MARGINAL: Better but still some shared structure.")
    else:
        print(f"  ❌ STILL CONFLATING: May need fundamentally different math or merge.")
    
    return results, best_pair, best_overlap


