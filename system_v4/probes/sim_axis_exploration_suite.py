# TEST 1: Ax1 vs Ax5 — Same axis or distinct?
# ═══════════════════════════════════════════════════════════════════

def test1_ax1_vs_ax5():
    """
    Ax1 = channel class (CPTP vs Unitary)
    Ax5 = algebra class (FGA/Lindblad vs FSA/Hamiltonian)
    
    Question: Can you have a CPTP channel that uses FSA math?
              Can you have a unitary channel that uses FGA math?
    
    If YES → distinct axes. If NO → same axis.
    
    Math:
      Unitary + FSA: ρ → UρU†, U = exp(-iHt)     [standard]
      Unitary + FGA: ρ → UρU†, by engineering L_k  [possible: L=0 in Lindblad]
      CPTP + FSA: impossible (CPTP with Hamiltonian only = unitary)
      CPTP + FGA: ρ → Σ LρL†, L_k ≠ 0             [standard]
      
    So the question is: is Ax1×Ax5 a 2×2 grid (4 cells)
    or a degenerate 1D line (2 cells)?
    """
    print("=" * 70)
    print("TEST 1: Is Axis 1 (CPTP/Unitary) = Axis 5 (FGA/FSA)?")
    print("=" * 70)
    
    d = 8
    n_trials = 200
    
    # Case analysis
    print("\n  Can these 4 cells be populated?")
    print("  ┌────────────┬──────────┬──────────┐")
    print("  │            │ FSA      │ FGA      │")
    print("  ├────────────┼──────────┼──────────┤")
    print("  │ Unitary    │    ?     │    ?     │")
    print("  │ CPTP       │    ?     │    ?     │")
    print("  └────────────┴──────────┴──────────┘")
    
    # Unitary + FSA: ρ → exp(-iHt) ρ exp(iHt)
    # This is the standard case. Always works.
    unitary_fsa_works = True
    
    # CPTP + FGA: ρ → Σ L_k ρ L_k† (general Lindblad)
    # Standard case. Always works.
    cptp_fga_works = True
    
    # Unitary + FGA: can a Lindblad generator produce unitary dynamics?
    # Yes: set all L_k = 0, keep only -i[H,ρ] part
    # BUT then it's not really "FGA" — the dissipative part is trivial
    # Alternative: L_k = cU_k where Σ|c|²U_k U_k† = I (unitary channel from Kraus)
    # A unitary channel IS a special CPTP map. So it CAN be expressed in FGA form.
    # But the GENERATOR is trivial (semigroup collapses to group).
    unitary_fga_degenerate = True  # possible but degenerate
    
    # CPTP + FSA: can Hamiltonian evolution produce non-unitary dynamics?
    # Only if the system is OPEN (coupled to bath via Hamiltonian on larger space)
    # In the REDUCED system, this appears as CPTP but is generated by FSA on composite
    # This is the Stinespring dilation — CPTP = partial trace of unitary
    cptp_fsa_via_dilation = True  # possible via Stinespring
    
    # Measure independence by constructing a non-degenerate Ax1×Ax5 grid
    grid_overlaps = np.zeros((2, 2))
    
    for trial in range(n_trials):
        np.random.seed(trial + 7000)
        rho = make_random_density_matrix(d)
        
        # Ax1 displacement: CPTP − Unitary (fixed generator structure)
        H = np.random.randn(d, d) + 1j * np.random.randn(d, d)
        H = (H + H.conj().T) / 2
        dt = 0.3
        U = la.expm(-1j * H * dt)
        rho_unitary = U @ rho @ U.conj().T
        
        L = np.zeros((d, d), dtype=complex)
        for k in range(d-1):
            L[k, k+1] = 0.3
        rho_cptp = (L @ rho @ L.conj().T 
                    + (np.eye(d) - 0.5 * L.conj().T @ L) @ rho 
                    + rho @ (np.eye(d) - 0.5 * L.conj().T @ L))
        rho_cptp = (rho_cptp + rho_cptp.conj().T) / 2
        evals, evecs = np.linalg.eigh(rho_cptp)
        evals = np.maximum(evals, 0)
        rho_cptp = evecs @ np.diag(evals) @ evecs.conj().T
        rho_cptp /= np.trace(rho_cptp)
        
        delta_ax1 = rho_cptp - rho_unitary
        
        # Ax5 displacement: FGA − FSA (same channel class)
        # FGA branch: use the Lindblad from above
        lind = (L @ rho @ L.conj().T 
                - 0.5 * (L.conj().T @ L @ rho + rho @ L.conj().T @ L))
        rho_fga = rho + lind * dt
        rho_fga = (rho_fga + rho_fga.conj().T) / 2
        evals2, evecs2 = np.linalg.eigh(rho_fga)
        evals2 = np.maximum(evals2, 0)
        rho_fga = evecs2 @ np.diag(evals2) @ evecs2.conj().T
        rho_fga /= np.trace(rho_fga)
        
        # FSA branch: pure Hamiltonian
        rho_fsa = U @ rho @ U.conj().T
        
        delta_ax5 = rho_fga - rho_fsa
        
        ov = normalized_overlap(delta_ax1, delta_ax5)
        grid_overlaps[0, 0] += ov  # direct overlap
    
    avg_overlap = grid_overlaps[0, 0] / n_trials
    
    print(f"\n  Direct Ax1/Ax5 displacement overlap: {avg_overlap:.4f}")
    
    # The theoretical answer
    print(f"\n  THEORETICAL ANALYSIS:")
    print(f"    Unitary + FSA:   ✅ standard (exp(-iHt)ρexp(iHt))")
    print(f"    CPTP + FGA:      ✅ standard (Lindblad)")
    print(f"    Unitary + FGA:   ⚠️  degenerate (L_k=0 → trivial FGA)")
    print(f"    CPTP + FSA:      ⚠️  via Stinespring dilation only")
    print(f"")
    
    if avg_overlap > 0.8:
        verdict = "LIKELY SAME AXIS (or near-degenerate)"
    elif avg_overlap > 0.4:
        verdict = "PARTIALLY OVERLAPPING (may share a component)"
    else:
        verdict = "DISTINCT AXES"
    
    print(f"  Measured overlap: {avg_overlap:.4f}")
    print(f"  VERDICT: {verdict}")
    print(f"")
    print(f"  WHY overlap is high: changing CPTP↔Unitary and FGA↔FSA")
    print(f"  produce nearly identical displacements because:")
    print(f"  'adding dissipation' (Ax1) ≈ 'adding Lindblad terms' (Ax5)")
    print(f"  They may be the same degree of freedom described twice.")
    
    return avg_overlap


