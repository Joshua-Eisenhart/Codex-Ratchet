# TEST 3: Outer/Inner Torus as Independent Axis Candidate
# ═══════════════════════════════════════════════════════════════════

def test3_outer_inner_axis():
    """
    Candidate new axis: outer/inner torus nesting (η parameter).
    
    η controls the relative size of the two S¹ circles in the torus T²(η):
      η = π/8:  inner torus (R_major ≈ 0.92, R_minor ≈ 0.38)
      η = π/4:  Clifford torus (R_major = R_minor ≈ 0.71)
      η = 3π/8: outer torus (R_major ≈ 0.38, R_minor ≈ 0.92)
    
    Question: Is "move between tori" (change η) independent of:
      - Chirality (Axis 3, ±G)
      - Action side (Axis 6, Aρ/ρA)
      - Channel class (Axis 1, CPTP/Unitary)
    
    If yes → it IS a new independent axis.
    """
    print("\n" + "=" * 70)
    print("TEST 3: Is Outer/Inner Torus (η) an Independent Axis?")
    print("=" * 70)
    
    if not HAS_HOPF:
        print("  ⚠️  hopf_manifold.py not importable. Using fallback geometry.")
    
    d = 2  # d=2 for Hopf/Weyl geometry (spinor space)
    n_trials = 200
    
    def torus_displace(rho, d_val):
        """Displacement from changing torus nesting η."""
        # Inner torus state
        eta_inner = np.pi / 8
        t1, t2 = np.random.rand() * 2 * np.pi, np.random.rand() * 2 * np.pi
        
        if HAS_HOPF:
            q_inner = torus_coordinates(eta_inner, t1, t2)
            q_outer = torus_coordinates(3 * np.pi / 8, t1, t2)
            rho_inner = coherent_state_density(q_inner)
            rho_outer = coherent_state_density(q_outer)
        else:
            # Fallback: model η change as mixing parameter
            rho_inner = rho  # as given (pure-ish)
            rho_outer = 0.3 * rho + 0.7 * np.eye(d_val) / d_val  # more mixed
        
        return rho_outer - rho_inner
    
    def chirality_displace(rho, d_val):
        """Axis 3: Weyl L vs R (using U vs U* formulation)."""
        H = np.random.randn(d_val, d_val) + 1j * np.random.randn(d_val, d_val)
        H = (H + H.conj().T) / 2
        U = la.expm(-1j * H * 0.5)
        rho_L = U @ rho @ U.conj().T
        rho_R = np.conj(U) @ rho @ np.conj(U).T
        return rho_L - rho_R
    
    def action_displace(rho, d_val):
        """Axis 6: Aρ vs ρA."""
        A = np.random.randn(d_val, d_val) + 1j * np.random.randn(d_val, d_val)
        A /= np.linalg.norm(A)
        return A @ rho - rho @ A
    
    def channel_displace(rho, d_val):
        """Axis 1: CPTP vs Unitary."""
        H = np.random.randn(d_val, d_val) + 1j * np.random.randn(d_val, d_val)
        H = (H + H.conj().T) / 2
        U = la.expm(-1j * H * 0.3)
        rho_uni = U @ rho @ U.conj().T
        gamma = 0.3
        K0 = np.diag([1.0, np.sqrt(1-gamma)]).astype(complex)
        K1 = np.array([[0, np.sqrt(gamma)], [0, 0]], dtype=complex)
        rho_cptp = K0 @ rho @ K0.conj().T + K1 @ rho @ K1.conj().T
        rho_cptp /= np.trace(rho_cptp)
        return rho_cptp - rho_uni
    
    axes = [
        ("Torus η (outer/inner)", torus_displace),
        ("Ax3 chirality (U/U*)", chirality_displace),
        ("Ax6 action (Aρ/ρA)", action_displace),
        ("Ax1 channel (CPTP/U)", channel_displace),
    ]
    
    n_axes = len(axes)
    overlap_matrix = np.zeros((n_axes, n_axes))
    
    for trial in range(n_trials):
        np.random.seed(trial + 9000)
        rho = make_random_density_matrix(d)
        disps = []
        for _, fn in axes:
            np.random.seed(trial + 9000)
            disps.append(fn(rho, d))
        
        for i in range(n_axes):
            for j in range(i+1, n_axes):
                ov = normalized_overlap(disps[i], disps[j])
                overlap_matrix[i, j] += ov
                overlap_matrix[j, i] += ov
    
    overlap_matrix /= n_trials
    
    print(f"\n  Overlap matrix (d={d}, {n_trials} trials):")
    for i in range(n_axes):
        row = f"    {axes[i][0]:25s}"
        for j in range(n_axes):
            if i == j:
                row += "    ---  "
            else:
                v = overlap_matrix[i, j]
                m = "✅" if v < 0.1 else ("⚠️" if v < 0.3 else "❌")
                row += f"  {v:.3f}{m}"
        print(row)
    
    # Check if torus η is independent
    torus_row = overlap_matrix[0, :]
    max_ov = max(torus_row[1:])
    
    print(f"\n  Torus η max overlap with other axes: {max_ov:.4f}")
    if max_ov < 0.1:
        print(f"  ✅ INDEPENDENT: Outer/inner IS a genuinely new axis")
    elif max_ov < 0.3:
        print(f"  ⚠️  MARGINAL: Partially independent, needs better construction")
    else:
        print(f"  ❌ DEPENDENT: Outer/inner is NOT independent of existing axes")
    
    return overlap_matrix


