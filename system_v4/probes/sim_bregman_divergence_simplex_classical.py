#!/usr/bin/env python3
"""Classical Bregman divergence on the probability simplex.

Lane B classical_baseline: Bregman divergence D_phi(p||q) generated by a
strictly convex phi on the open simplex.
"""
import json
import os
from typing import Literal, Optional
import numpy as np

classification: Literal["classical_baseline", "canonical"] = "classical_baseline"
divergence_log: Optional[str] = (
    "Classical Bregman divergence D_phi(p||q) = phi(p) - phi(q) - <grad phi(q), p-q> "
    "is defined on a commutative simplex using a scalar potential phi. Quantum "
    "Bregman divergence requires an operator-convex trace functional and honors "
    "noncommutativity of rho, sigma via symmetric logarithmic derivatives. The "
    "classical substrate cannot distinguish states that share eigenvalues but "
    "differ in eigenbasis: quantum coherence between eigenbases is invisible here."
)

