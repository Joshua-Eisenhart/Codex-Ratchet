# A2 ROOT SYSTEM AND WEYL GROUP SETUP
# =====================================================================
#
# Simple roots of A2 in standard embedding (2D):
#   alpha1 = (1, 0)
#   alpha2 = (-1/2, sqrt(3)/2)
# All 6 roots: ±alpha1, ±alpha2, ±(alpha1+alpha2)
#
# Simple reflections (as 2x2 matrices):
#   s_i(v) = v - 2*<v,alpha_i>/<alpha_i,alpha_i> * alpha_i
#
# Cartan matrix A2: [[2,-1],[-1,2]]

SQRT3_2 = math.sqrt(3.0) / 2.0

# Simple roots as numpy-backed torch tensors
ALPHA1 = torch.tensor([1.0, 0.0], dtype=torch.float64)
ALPHA2 = torch.tensor([-0.5, SQRT3_2], dtype=torch.float64)
ALPHA3 = ALPHA1 + ALPHA2  # alpha1 + alpha2


def simple_reflection_matrix(alpha: torch.Tensor) -> torch.Tensor:
    """Return the 2x2 matrix for the reflection s_alpha."""
    n = alpha / torch.dot(alpha, alpha)
    # s_alpha(v) = v - 2 <v,alpha>/<alpha,alpha> alpha
    # Matrix: I - 2 * (alpha ⊗ alpha) / <alpha,alpha>
    return torch.eye(2, dtype=torch.float64) - 2.0 * torch.outer(n, alpha)


def generate_weyl_group_a2():
    """Generate all 6 elements of W(A2) as 2x2 matrices."""
    S1 = simple_reflection_matrix(ALPHA1)
    S2 = simple_reflection_matrix(ALPHA2)
    elements = []
    # W(A2) = {e, s1, s2, s1s2, s2s1, s1s2s1}
    I = torch.eye(2, dtype=torch.float64)
    elements.append(("e", I))
    elements.append(("s1", S1))
    elements.append(("s2", S2))
    elements.append(("s1s2", S1 @ S2))
    elements.append(("s2s1", S2 @ S1))
    elements.append(("s1s2s1", S1 @ S2 @ S1))
    return elements, S1, S2


ALL_ROOTS = [
    ALPHA1, -ALPHA1,
    ALPHA2, -ALPHA2,
    ALPHA3, -ALPHA3,
]

ALL_ROOTS_SET = [tuple(r.tolist()) for r in ALL_ROOTS]


def root_in_set(v, root_set, tol=1e-8):
    """Check if vector v (as tensor) matches any root in root_set (list of tuples)."""
    vt = tuple(v.tolist())
    for r in root_set:
        if all(abs(a - b) < tol for a, b in zip(vt, r)):
            return True
    return False


