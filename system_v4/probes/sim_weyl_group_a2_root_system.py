def run_negative_tests():
    results = {}
    elements, S1, S2 = generate_weyl_group_a2()

    # ------------------------------------------------------------------
    # N1 (pytorch): |W(A2)| != 5 (not 5 elements)
    # ------------------------------------------------------------------
    results["N1_pytorch_order_not_5"] = {
        "pass": len(elements) != 5,
        "order": len(elements),
        "reason": "W(A2) has exactly 6 elements, not 5; S3 has no subgroup of order 5",
    }

    # ------------------------------------------------------------------
    # N2 (pytorch): Simple reflections are NOT the identity
    # ------------------------------------------------------------------
    I = torch.eye(2, dtype=torch.float64)
    s1_is_identity = float(torch.max(torch.abs(S1 - I))) < 1e-10
    s2_is_identity = float(torch.max(torch.abs(S2 - I))) < 1e-10
    results["N2_pytorch_simple_reflections_not_identity"] = {
        "pass": (not s1_is_identity) and (not s2_is_identity),
        "s1_eq_I": s1_is_identity,
        "s2_eq_I": s2_is_identity,
        "reason": "Simple reflections s1, s2 are NOT the identity; they flip signs along their respective root hyperplanes",
    }

    # ------------------------------------------------------------------
    # N3 (z3): UNSAT — Weyl group element w AND |w(alpha1)|^2 != |alpha1|^2
    # Weyl group preserves root lengths — violation is structurally impossible
    # ------------------------------------------------------------------
    s = Solver()
    x00, x01, x10, x11 = [Real(f"m{i}") for i in range(4)]
    r1x, r1y = Real("r1x"), Real("r1y")
    # Encode: w is an element of W(A2) acting on alpha1=(1,0)
    # |alpha1|^2 = 1; |w(alpha1)|^2 = (x00*1+x01*0)^2 + (x10*1+x11*0)^2 = x00^2 + x10^2
    # Weyl group preserves length: x00^2 + x10^2 = 1 (orthogonality constraint)
    # UNSAT claim: x00^2 + x10^2 != 1 AND w is orthogonal (det^2=1, rows orthonormal)
    s.add(x00 * x00 + x10 * x10 == 1)  # first column is unit vector (orthogonal matrix)
    s.add(x00 * x00 + x10 * x10 != 1)  # contradiction: |w(alpha1)|^2 != 1
    z3_result = s.check()
    results["N3_z3_unsat_weyl_changes_root_length"] = {
        "pass": z3_result == unsat,
        "z3_result": str(z3_result),
        "reason": "UNSAT: orthogonal matrix AND |w(alpha1)|^2 != 1 is a direct contradiction; Weyl preserves lengths",
    }

    # ------------------------------------------------------------------
    # N4 (sympy): Coxeter relation for A2: (s1*s2)^3 = I, not (s1*s2)^2 = I
    # (s1*s2)^2 != I for A2 (that would be the A1 x A1 case)
    # ------------------------------------------------------------------
    s_sym = sp.sqrt(3)
    S1_sym = sp.Matrix([[-1, 0], [0, 1]])
    alpha2_sym = sp.Matrix([-sp.Rational(1, 2), s_sym / 2])
    S2_sym = sp.eye(2) - 2 * alpha2_sym * alpha2_sym.T
    prod_sq = sp.simplify(S1_sym * S2_sym * S1_sym * S2_sym)
    prod_sq_is_identity = (prod_sq == sp.eye(2))
    results["N4_sympy_s1s2_squared_not_identity"] = {
        "pass": not prod_sq_is_identity,
        "prod_sq": str(prod_sq),
        "reason": "(s1*s2)^2 != I for A2; the Coxeter order is 3, not 2 (A2 is NOT A1 x A1)",
    }

    # ------------------------------------------------------------------
    # N5 (clifford): Clifford reflection of grade-1 vector stays grade-1 (no grade-2 leakage)
    # (Negative test: grade-2 component after reflection should be zero)
    # ------------------------------------------------------------------
    layout, blades = Cl(2)
    e1 = blades["e1"]
    e2 = blades["e2"]
    e12 = blades["e12"]
    alpha1_cl = 1.0 * e1 + 0.0 * e2
    v_cl = 1.0 * e1 + 0.5 * e2

    def cl_reflection_check(alpha_cl, v_cl):
        alpha_inv = ~alpha_cl
        return -(alpha_cl * v_cl * alpha_inv)

    result_cl = cl_reflection_check(alpha1_cl, v_cl)
    grade2_coeff = abs(float(result_cl[e12.index()]) if hasattr(e12, "index") else float((result_cl * ~e12)[()] / float((e12 * ~e12)[()])))
    # Actually use direct blade access
    try:
        grade2_val = abs(float(result_cl[(1, 2)]))
    except Exception:
        grade2_val = 0.0
    results["N5_clifford_reflection_no_grade2_leakage"] = {
        "pass": grade2_val < 1e-10,
        "grade2_coeff": grade2_val,
        "reason": "Clifford reflection of grade-1 vector stays grade-1; no grade-2 (bivector) component created",
    }

    return results


