def run_positive_tests():
    results = {}
    elements, S1, S2 = generate_weyl_group_a2()

    # ------------------------------------------------------------------
    # P1 (pytorch): W(A2) has exactly 6 elements
    # ------------------------------------------------------------------
    results["P1_pytorch_weyl_group_order_6"] = {
        "pass": len(elements) == 6,
        "order": len(elements),
        "reason": "W(A2) = S3 has order 6; generated by {s1, s2} with braid+order-2 relations",
    }

    # ------------------------------------------------------------------
    # P2 (pytorch): All 6 roots obtained by applying W(A2) to alpha1
    # ------------------------------------------------------------------
    orbit_alpha1 = set()
    for name, M in elements:
        img = M @ ALPHA1
        img_rounded = tuple(round(x, 8) for x in img.tolist())
        orbit_alpha1.add(img_rounded)
    orbit_matches_all_roots = all(
        root_in_set(r, [tuple(round(x, 8) for x in t) for t in [r2.tolist() for r2 in ALL_ROOTS]])
        for r in [torch.tensor(list(t)) for t in orbit_alpha1]
    )
    results["P2_pytorch_orbit_of_alpha1_is_all_6_roots"] = {
        "pass": len(orbit_alpha1) == 6,
        "orbit_size": len(orbit_alpha1),
        "reason": "W(A2) acts transitively on the 6-root set; orbit of alpha1 = all 6 roots",
    }

    # ------------------------------------------------------------------
    # P3 (pytorch): Each Weyl element is orthogonal (det = +/-1, M^T M = I)
    # ------------------------------------------------------------------
    all_orthogonal = True
    dets = []
    for name, M in elements:
        MtM = M.T @ M
        I = torch.eye(2, dtype=torch.float64)
        ortho_err = float(torch.max(torch.abs(MtM - I)))
        det_val = float(torch.det(M))
        dets.append(det_val)
        if ortho_err > 1e-10 or abs(abs(det_val) - 1.0) > 1e-10:
            all_orthogonal = False
    results["P3_pytorch_weyl_elements_orthogonal"] = {
        "pass": all_orthogonal,
        "dets": [round(d, 10) for d in dets],
        "reason": "All W(A2) elements are orthogonal transformations: M^T M = I, det = +/-1",
    }

    # ------------------------------------------------------------------
    # P4 (pytorch): Each Weyl element maps roots to roots (permutation)
    # ------------------------------------------------------------------
    all_permute = True
    for name, M in elements:
        for root in ALL_ROOTS:
            img = M @ root
            if not root_in_set(img, ALL_ROOTS_SET):
                all_permute = False
                break
    results["P4_pytorch_weyl_group_permutes_roots"] = {
        "pass": all_permute,
        "reason": "Every W(A2) element maps each root to another root in the 6-root set",
    }

    # ------------------------------------------------------------------
    # P5 (sympy): Braid relation s1*s2*s1 = s2*s1*s2
    # ------------------------------------------------------------------
    # Use 2x2 rational matrices for exactness
    s = sp.sqrt(3)
    # s1 for alpha1 = (1,0): reflects x -> -x, keeps y
    S1_sym = sp.Matrix([[-1, 0], [0, 1]])
    # s2 for alpha2 = (-1/2, sqrt(3)/2):
    # s2(v) = v - 2*<v,alpha2>/<alpha2,alpha2>*alpha2
    # <alpha2,alpha2> = 1/4 + 3/4 = 1
    alpha2_sym = sp.Matrix([-sp.Rational(1, 2), s / 2])
    S2_sym = sp.eye(2) - 2 * alpha2_sym * alpha2_sym.T
    lhs = S1_sym * S2_sym * S1_sym
    rhs = S2_sym * S1_sym * S2_sym
    braid_holds = (sp.simplify(lhs - rhs) == sp.zeros(2, 2))
    results["P5_sympy_braid_relation_s1s2s1_eq_s2s1s2"] = {
        "pass": bool(braid_holds),
        "lhs": str(lhs),
        "rhs": str(rhs),
        "reason": "A2 braid relation s1*s2*s1 = s2*s1*s2 verified symbolically (Coxeter relation for A2)",
    }

    # ------------------------------------------------------------------
    # P6 (sympy): Cartan matrix A2 = [[2,-1],[-1,2]]
    # Cartan matrix entry: A_{ij} = 2 <alpha_i, alpha_j> / <alpha_j, alpha_j>
    # ------------------------------------------------------------------
    # With alpha1=(1,0), alpha2=(-1/2, sqrt(3)/2):
    # <alpha1,alpha2> = -1/2; <alpha1,alpha1>=1; <alpha2,alpha2>=1
    # A_{12} = 2*(-1/2)/1 = -1; A_{21} = 2*(-1/2)/1 = -1
    alpha1_sym = sp.Matrix([1, 0])
    a11 = 2 * alpha1_sym.dot(alpha1_sym) // alpha1_sym.dot(alpha1_sym)  # = 2
    a12 = 2 * alpha1_sym.dot(alpha2_sym) // alpha2_sym.dot(alpha2_sym)
    a21 = 2 * alpha2_sym.dot(alpha1_sym) // alpha1_sym.dot(alpha1_sym)
    a22 = 2 * alpha2_sym.dot(alpha2_sym) // alpha2_sym.dot(alpha2_sym)  # = 2
    cartan = sp.Matrix([[a11, a12], [a21, a22]])
    expected_cartan = sp.Matrix([[2, -1], [-1, 2]])
    cartan_correct = (sp.simplify(cartan - expected_cartan) == sp.zeros(2, 2))
    results["P6_sympy_cartan_matrix_a2"] = {
        "pass": bool(cartan_correct),
        "cartan": str(cartan),
        "expected": "[[2,-1],[-1,2]]",
        "reason": "A2 Cartan matrix = [[2,-1],[-1,2]]; off-diagonal -1 encodes 120-degree angle between simple roots",
    }

    # ------------------------------------------------------------------
    # P7 (clifford): Clifford reflections map roots to roots
    # s_alpha(v) = -alpha * v * alpha^{-1} in Cl(2,0)
    # ------------------------------------------------------------------
    layout, blades = Cl(2)
    e1 = blades["e1"]
    e2 = blades["e2"]

    def to_cl(v_torch):
        vx, vy = float(v_torch[0]), float(v_torch[1])
        return vx * e1 + vy * e2

    def cl_reflection(alpha_cl, v_cl):
        """Clifford reflection: s_alpha(v) = -alpha * v * alpha_inv."""
        alpha_inv = ~alpha_cl  # reverse = inverse for unit vectors in Cl(2,0)
        return -(alpha_cl * v_cl * alpha_inv)

    def cl_to_vec(mv):
        """Extract grade-1 components from a multivector."""
        return (float(mv[blades["e1"].index()]), float(mv[blades["e2"].index()]))

    def vec_close_to_root(vx, vy, tol=1e-8):
        for rt in ALL_ROOTS:
            if abs(float(rt[0]) - vx) < tol and abs(float(rt[1]) - vy) < tol:
                return True
        return False

    # Wait — Clifford's index() isn't standard. Use value extraction via blade dict.
    def cl_grade1(mv):
        e1_coeff = float((mv * ~e1)[()]) / float((e1 * ~e1)[()])
        e2_coeff = float((mv * ~e2)[()]) / float((e2 * ~e2)[()])
        return e1_coeff, e2_coeff

    alpha1_cl = to_cl(ALPHA1)
    alpha2_cl = to_cl(ALPHA2)

    all_cl_map_to_roots = True
    for root_t in ALL_ROOTS:
        v_cl = to_cl(root_t)
        r1 = cl_reflection(alpha1_cl, v_cl)
        r2 = cl_reflection(alpha2_cl, v_cl)
        # Extract grade-1 parts
        r1x, r1y = cl_grade1(r1)
        r2x, r2y = cl_grade1(r2)
        if not vec_close_to_root(r1x, r1y):
            all_cl_map_to_roots = False
        if not vec_close_to_root(r2x, r2y):
            all_cl_map_to_roots = False

    results["P7_clifford_reflections_map_roots_to_roots"] = {
        "pass": all_cl_map_to_roots,
        "reason": "Clifford reflection -alpha*v*alpha^{-1} maps each A2 root to another A2 root",
    }

    # ------------------------------------------------------------------
    # P8 (rustworkx): Cayley graph of W(A2) has 6 nodes, correct edge count
    # ------------------------------------------------------------------
    # Build Cayley graph: nodes = group elements (by index), edges = s1/s2 actions
    g = rx.PyDiGraph()
    elem_names = [name for name, _ in elements]
    node_indices = {name: g.add_node(name) for name in elem_names}

    # Compute action of s1 on each element (left multiplication: s1 * w)
    elem_mats = {name: M for name, M in elements}

    def mat_to_name(M, elem_mats, tol=1e-8):
        for n, E in elem_mats.items():
            if float(torch.max(torch.abs(M - E))) < tol:
                return n
        return None

    s1_mat = elem_mats["s1"]
    s2_mat = elem_mats["s2"]
    for name in elem_names:
        w = elem_mats[name]
        img_s1 = s1_mat @ w
        img_s2 = s2_mat @ w
        n1 = mat_to_name(img_s1, elem_mats)
        n2 = mat_to_name(img_s2, elem_mats)
        if n1:
            g.add_edge(node_indices[name], node_indices[n1], "s1")
        if n2:
            g.add_edge(node_indices[name], node_indices[n2], "s2")

    results["P8_rustworkx_cayley_graph_6_nodes"] = {
        "pass": g.num_nodes() == 6 and g.num_edges() == 12,
        "num_nodes": g.num_nodes(),
        "num_edges": g.num_edges(),
        "reason": "Cayley graph of W(A2) = S3 with generators {s1,s2}: 6 nodes, 12 directed edges (2 per node)",
    }

    # ------------------------------------------------------------------
    # P9 (xgi): Hyperedges encode algebraic constraints
    # ------------------------------------------------------------------
    H = xgi.Hypergraph()
    H.add_nodes_from(range(6))  # 6 group elements
    # Hyperedge: {s1_idx=1, s2_idx=2, w0_idx=5} (longest element = s1*s2*s1)
    H.add_edge([1, 2, 5])
    # Hyperedge: roots {alpha1, alpha2, -(alpha1+alpha2)} as indices into ALL_ROOTS
    # alpha1=idx0, alpha2=idx2, -(alpha1+alpha2)=idx5
    H.add_edge([0, 2, 5])
    results["P9_xgi_hyperedges_encode_weyl_constraints"] = {
        "pass": H.num_edges == 2 and H.num_nodes == 6,
        "num_edges": H.num_edges,
        "num_nodes": H.num_nodes,
        "reason": "XGI hyperedges: {s1,s2,w0} and {alpha1,alpha2,-(alpha1+alpha2)} encode A2 Weyl structure",
    }

    return results


