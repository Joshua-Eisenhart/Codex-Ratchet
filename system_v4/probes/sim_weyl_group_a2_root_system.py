    w0_sq_is_identity = float(torch.max(torch.abs(w0_sq - I))) < 1e-10
    results["B2_pytorch_longest_element_order_2"] = {
        "pass": w0_sq_is_identity,
        "max_err": float(torch.max(torch.abs(w0_sq - I))),
        "reason": "w0^2 = I in W(A2): the longest element is an involution (order 2)",
    }

    # ------------------------------------------------------------------
    # B3 (sympy): Coxeter relation (s1*s2)^3 = I for A2
    # ------------------------------------------------------------------
    s_sym = sp.sqrt(3)
    S1_sym = sp.Matrix([[-1, 0], [0, 1]])
    alpha2_sym = sp.Matrix([-sp.Rational(1, 2), s_sym / 2])
    S2_sym = sp.eye(2) - 2 * alpha2_sym * alpha2_sym.T
    prod_cube = sp.simplify((S1_sym * S2_sym) ** 3)
    coxeter_holds = (prod_cube == sp.eye(2))
    results["B3_sympy_coxeter_relation_s1s2_order_3"] = {
        "pass": bool(coxeter_holds),
        "prod_cube": str(prod_cube),
        "reason": "(s1*s2)^3 = I for A2; Coxeter order of the pair (s1,s2) is exactly 3",
    }

    # ------------------------------------------------------------------
    # B4 (rustworkx): Cayley graph is strongly connected (all elements reachable from e)
    # ------------------------------------------------------------------
    g = rx.PyDiGraph()
    elem_names = [name for name, _ in elements]
    node_indices = {name: g.add_node(name) for name in elem_names}

    def mat_to_name(M, em, tol=1e-8):
        for n, E in em.items():
            if float(torch.max(torch.abs(M - E))) < tol:
                return n
        return None

    s1_mat = elem_mats["s1"]
    s2_mat = elem_mats["s2"]
    for name in elem_names:
        w = elem_mats[name]
        n1 = mat_to_name(s1_mat @ w, elem_mats)
        n2 = mat_to_name(s2_mat @ w, elem_mats)
        if n1:
            g.add_edge(node_indices[name], node_indices[n1], "s1")
        if n2:
            g.add_edge(node_indices[name], node_indices[n2], "s2")

    # Check strong connectivity of the Cayley graph
    is_strongly = rx.is_strongly_connected(g)
    results["B4_rustworkx_cayley_graph_connected"] = {
        "pass": is_strongly,
        "is_strongly_connected": is_strongly,
        "reason": "Cayley graph of W(A2) is strongly connected: all 6 elements reachable from identity via s1, s2",
    }

    # ------------------------------------------------------------------
    # B5 (xgi): Hyperedge {alpha1, alpha2, alpha1+alpha2} is the positive root triple
    # ------------------------------------------------------------------
    H = xgi.Hypergraph()
    H.add_nodes_from(range(6))  # 6 roots
    # Positive roots: alpha1 (idx 0), alpha2 (idx 2), alpha3=alpha1+alpha2 (idx 4)
    H.add_edge([0, 2, 4])
    results["B5_xgi_positive_root_triple_hyperedge"] = {
        "pass": H.num_edges == 1 and 0 in list(H.edges.members()[0]),
        "num_edges": H.num_edges,
        "reason": "XGI: positive root triple {alpha1, alpha2, alpha1+alpha2} forms a 3-way hyperedge",
    }

    # ------------------------------------------------------------------
    # B6 (z3): SAT — there exists a Weyl element (the identity) that fixes all roots
    # (SANITY CHECK: identity element preserves all roots)
    # ------------------------------------------------------------------
    s_z3 = Solver()
    r_len = Real("root_length")
    # Identity transformation: each root mapped to itself, |w(r)|^2 = |r|^2 = 1
    s_z3.add(r_len == 1)  # root length = 1
    s_z3.add(r_len == 1)  # image also has length 1
    # consistent => SAT
    z3_sat_result = s_z3.check()
    results["B6_z3_sat_identity_preserves_roots"] = {
        "pass": z3_sat_result == sat,
        "z3_result": str(z3_sat_result),
        "reason": "SAT: identity element preserves all root lengths; consistent encoding",
    }

    return results


