#
# B2 root system in R^2 (standard coordinates e1=(1,0), e2=(0,1)):
#   Short roots: ±e1=(±1,0), ±e2=(0,±1)  [length = 1]
#   Long roots:  ±(e1+e2)=(±1,±1), ±(e1-e2)=(±1,∓1)  [length = sqrt(2)]
#
# Simple roots:
#   alpha1 = e1 = (1, 0)   [short root, length 1]
#   alpha2 = -e1 + e2 = (-1, 1)  [long root ... wait]
#
# Standard B2 simple roots:
#   alpha1 = e1 - e2 = (1, -1)  [long, length sqrt(2)]
#   alpha2 = e2 = (0, 1)        [short, length 1]
#
# Cartan matrix B2: A_{12} = 2<alpha1,alpha2>/<alpha2,alpha2> = 2*(−1)/1 = -2
#                   A_{21} = 2<alpha2,alpha1>/<alpha1,alpha1> = 2*(−1)/2 = -1
# => [[2,-1],[-2,2]]
#
# Simple reflections:
#   s1: reflection perpendicular to alpha1 = (1,-1)/sqrt(2)
#   s2: reflection perpendicular to alpha2 = (0,1)
#
# All 8 roots:
#   ±e1 = ±(1,0), ±e2 = ±(0,1)   [short, length 1]
#   ±(e1+e2) = ±(1,1), ±(e1-e2) = ±(1,-1)  [long, length sqrt(2)]

SQRT2 = math.sqrt(2.0)

# Short roots
ROOT_e1  = torch.tensor([ 1.0,  0.0], dtype=torch.float64)
ROOT_me1 = torch.tensor([-1.0,  0.0], dtype=torch.float64)
ROOT_e2  = torch.tensor([ 0.0,  1.0], dtype=torch.float64)
ROOT_me2 = torch.tensor([ 0.0, -1.0], dtype=torch.float64)

# Long roots
ROOT_e1pe2  = torch.tensor([ 1.0,  1.0], dtype=torch.float64)
ROOT_me1me2 = torch.tensor([-1.0, -1.0], dtype=torch.float64)
ROOT_e1me2  = torch.tensor([ 1.0, -1.0], dtype=torch.float64)
ROOT_me1pe2 = torch.tensor([-1.0,  1.0], dtype=torch.float64)

ALL_ROOTS_B2 = [
    ROOT_e1, ROOT_me1, ROOT_e2, ROOT_me2,
    ROOT_e1pe2, ROOT_me1me2, ROOT_e1me2, ROOT_me1pe2,
]
ALL_ROOTS_B2_SET = [tuple(r.tolist()) for r in ALL_ROOTS_B2]

# Simple roots for B2
ALPHA1_B2 = ROOT_e1me2  # (1, -1), long root
ALPHA2_B2 = ROOT_e2     # (0, 1),  short root


def simple_reflection_matrix(alpha: torch.Tensor) -> torch.Tensor:
    """Return the 2x2 matrix for the reflection s_alpha."""
    # s_alpha(v) = v - 2 <v,alpha>/<alpha,alpha> alpha
    return torch.eye(2, dtype=torch.float64) - 2.0 * torch.outer(alpha, alpha) / torch.dot(alpha, alpha)


def generate_weyl_group_b2():
    """Generate all 8 elements of W(B2) as 2x2 matrices."""
    S1 = simple_reflection_matrix(ALPHA1_B2)
    S2 = simple_reflection_matrix(ALPHA2_B2)
    I = torch.eye(2, dtype=torch.float64)
    elements = []
    elements.append(("e",          I))
    elements.append(("s1",         S1))
    elements.append(("s2",         S2))
    elements.append(("s1s2",       S1 @ S2))
    elements.append(("s2s1",       S2 @ S1))
    elements.append(("s1s2s1",     S1 @ S2 @ S1))
    elements.append(("s2s1s2",     S2 @ S1 @ S2))
    elements.append(("s1s2s1s2",   S1 @ S2 @ S1 @ S2))
    return elements, S1, S2


def root_in_set_b2(v: torch.Tensor, root_set, tol=1e-8):
    vt = tuple(v.tolist())
    for r in root_set:
        if all(abs(a - b) < tol for a, b in zip(vt, r)):
            return True
    return False


