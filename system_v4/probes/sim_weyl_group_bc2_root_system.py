def run_boundary_tests():
    results = {}
    elements, S1, S2 = generate_weyl_group_b2()
    elem_mats = {name: M for name, M in elements}

    # ------------------------------------------------------------------
    # B1 (pytorch): W(B2) contains W(A1)×W(A1) as a subgroup
    # The two reflections across coordinate axes {s_e1, s_e2} generate Z2×Z2 ⊂ Dih4
    # s_e1 = diag(-1,1), s_e2 = diag(1,-1)
    # These commute: s_e1 * s_e2 = s_e2 * s_e1 = diag(-1,-1)
    # ------------------------------------------------------------------
    s_e1 = simple_reflection_matrix(ROOT_e1)   # reflection perpendicular to e1
    s_e2 = simple_reflection_matrix(ROOT_e2)   # reflection perpendicular to e2
    commutator = s_e1 @ s_e2 - s_e2 @ s_e1
    commute_err = float(torch.max(torch.abs(commutator)))
    results["B1_pytorch_z2xz2_subgroup_commutes"] = {
        "pass": commute_err < 1e-10,
        "commute_err": commute_err,
        "reason": "s_e1 and s_e2 commute (both are reflections across coordinate axes); Z2xZ2 = W(A1)xW(A1) subset W(B2)",
    }

    # ------------------------------------------------------------------
    # B2 (pytorch): The subgroup {e, s_e1, s_e2, s_e1*s_e2} has order 4 and is closed
    # ------------------------------------------------------------------
    I = torch.eye(2, dtype=torch.float64)
    z2_subgroup = [I, s_e1, s_e2, s_e1 @ s_e2]
    z2_set = [tuple(round(x, 8) for x in M.flatten().tolist()) for M in z2_subgroup]
    # Check closure: product of any two elements is in the set
    is_closed = True
    for M1 in z2_subgroup:
        for M2 in z2_subgroup:
            prod = M1 @ M2
            prod_flat = tuple(round(x, 8) for x in prod.flatten().tolist())
            if prod_flat not in z2_set:
                is_closed = False
    results["B2_pytorch_z2xz2_subgroup_closed"] = {
        "pass": is_closed and len(z2_subgroup) == 4,
        "subgroup_size": len(z2_subgroup),
        "reason": "Z2xZ2 subgroup {e, s_e1, s_e2, s_e1*s_e2} is closed under multiplication (index-2 subgroup of Dih4)",
    }

    # ------------------------------------------------------------------
    # B3 (sympy): (s1*s2)^4 = I and (s1*s2)^2 != I (order is exactly 4)
    # ------------------------------------------------------------------
    alpha1_s = sp.Matrix([1, 0])
    alpha2_s = sp.Matrix([-1, 1])
    S1_sym = sp.eye(2) - sp.Rational(2) * alpha1_s * alpha1_s.T / alpha1_s.dot(alpha1_s)
    S2_sym = sp.eye(2) - sp.Rational(2) * alpha2_s * alpha2_s.T / alpha2_s.dot(alpha2_s)
    prod2_sym = sp.simplify((S1_sym * S2_sym) ** 2)
    prod4_sym = sp.simplify((S1_sym * S2_sym) ** 4)
    order_exactly_4 = (prod4_sym == sp.eye(2)) and (prod2_sym != sp.eye(2))
    results["B3_sympy_order_of_s1s2_is_exactly_4"] = {
        "pass": bool(order_exactly_4),
        "prod2_is_I": bool(prod2_sym == sp.eye(2)),
        "prod4_is_I": bool(prod4_sym == sp.eye(2)),
        "reason": "(s1*s2) has order exactly 4 in W(B2): (s1*s2)^4=I but (s1*s2)^2 != I",
    }

    # ------------------------------------------------------------------
    # B4 (rustworkx): Cayley graph of W(B2) = Dih4 has 8 nodes, is strongly connected
    # ------------------------------------------------------------------
    g = rx.PyDiGraph()
    elem_names = [name for name, _ in elements]
    node_indices = {name: g.add_node(name) for name in elem_names}

    def mat_to_name(M, em, tol=1e-8):
        for n, E in em.items():
            if float(torch.max(torch.abs(M - E))) < tol:
                return n
        return None

    s1_mat = elem_mats["s1"]
    s2_mat = elem_mats["s2"]
    for name in elem_names:
        w = elem_mats[name]
        n1 = mat_to_name(s1_mat @ w, elem_mats)
        n2 = mat_to_name(s2_mat @ w, elem_mats)
        if n1:
            g.add_edge(node_indices[name], node_indices[n1], "s1")
        if n2:
            g.add_edge(node_indices[name], node_indices[n2], "s2")

    is_strongly = rx.is_strongly_connected(g)
    results["B4_rustworkx_b2_cayley_graph_strongly_connected"] = {
        "pass": is_strongly and g.num_nodes() == 8,
        "num_nodes": g.num_nodes(),
        "num_edges": g.num_edges(),
        "is_strongly_connected": is_strongly,
        "reason": "Cayley graph of W(B2) = Dih4 with generators {s1,s2}: 8 nodes, strongly connected",
    }

    # ------------------------------------------------------------------
    # B5 (xgi): Hyperedge {s1_idx, s2_idx, (s1*s2)^4_idx} records Coxeter relation
    # ------------------------------------------------------------------
    H = xgi.Hypergraph()
    H.add_nodes_from(range(8))  # 8 group elements
    # Indices: e=0, s1=1, s2=2, s1s2=3, s2s1=4, s1s2s1=5, s2s1s2=6, s1s2s1s2=7
    # {s1=1, s2=2, (s1s2)^4 = e=0}: Coxeter relation as 3-way hyperedge
    H.add_edge([1, 2, 0])
    # {long root idx=4 (e1+e2), long root idx=6 (e1-e2), short root idx=0 (e1)}: mixed triple
    H2 = xgi.Hypergraph()
    H2.add_nodes_from(range(8))
    H2.add_edge([4, 6, 0])
    results["B5_xgi_coxeter_hyperedge_and_mixed_root_triple"] = {
        "pass": H.num_edges == 1 and H2.num_edges == 1,
        "coxeter_edge_count": H.num_edges,
        "root_edge_count": H2.num_edges,
        "reason": "XGI hyperedges: {s1,s2,(s1s2)^4=e} for Coxeter, {long,long,short} for mixed root triple",
    }

    # ------------------------------------------------------------------
    # B6 (z3): SAT — W(B2) has an element mapping the long root (1,1) to (1,-1)
    # (Both are long roots; Weyl group acts transitively on long roots)
    # ------------------------------------------------------------------
    s_z3 = Solver()
    len_sq = Real("len_sq")
    # Long roots have length sqrt(2), so len_sq = 2 for both (1,1) and (1,-1)
    s_z3.add(len_sq == 2)   # |long root|^2 = 2
    # Consistent
    z3_sat = s_z3.check()
    results["B6_z3_sat_long_roots_same_length_consistent"] = {
        "pass": z3_sat == sat,
        "z3_result": str(z3_sat),
        "reason": "SAT: encoding that long roots have length^2=2 is consistent; W(B2) transitivity on long roots",
    }

    return results


