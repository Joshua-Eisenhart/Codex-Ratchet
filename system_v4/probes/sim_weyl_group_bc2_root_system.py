def run_positive_tests():
    results = {}
    elements, S1, S2 = generate_weyl_group_b2()

    # ------------------------------------------------------------------
    # P1 (pytorch): W(B2) has exactly 8 elements
    # ------------------------------------------------------------------
    results["P1_pytorch_weyl_group_b2_order_8"] = {
        "pass": len(elements) == 8,
        "order": len(elements),
        "reason": "W(B2) = Dih4 has order 8; generated by {s1,s2} with Coxeter relation (s1s2)^4=e",
    }

    # ------------------------------------------------------------------
    # P2 (pytorch): W(B2) has two orbits on roots — short and long.
    # Orbit of e2 (short) = 4 short roots. Orbit of e1+e2 (long) = 4 long roots.
    # B2 is NOT simply-laced, so the Weyl group is NOT transitive on all 8 roots.
    # ------------------------------------------------------------------
    short_roots = [ROOT_e1, ROOT_me1, ROOT_e2, ROOT_me2]
    long_roots = [ROOT_e1pe2, ROOT_me1me2, ROOT_e1me2, ROOT_me1pe2]
    short_root_set = [tuple(r.tolist()) for r in short_roots]
    long_root_set = [tuple(r.tolist()) for r in long_roots]

    orbit_short = set()
    orbit_long = set()
    for name, M in elements:
        img_short = M @ ROOT_e2
        orbit_short.add(tuple(round(x, 8) for x in img_short.tolist()))
        img_long = M @ ROOT_e1pe2
        orbit_long.add(tuple(round(x, 8) for x in img_long.tolist()))
    results["P2_pytorch_b2_two_orbits_short_and_long"] = {
        "pass": len(orbit_short) == 4 and len(orbit_long) == 4,
        "orbit_short_size": len(orbit_short),
        "orbit_long_size": len(orbit_long),
        "reason": (
            "B2 is not simply-laced: W(B2) has TWO orbits on roots — "
            "4 short roots (orbit of e2) and 4 long roots (orbit of e1+e2). "
            "NOT transitive on all 8 roots (unlike A2/simply-laced)."
        ),
    }

    # ------------------------------------------------------------------
    # P3 (pytorch): Each Weyl element is orthogonal (det = ±1, M^T M = I)
    # ------------------------------------------------------------------
    all_orthogonal = True
    dets = []
    for name, M in elements:
        MtM = M.T @ M
        I = torch.eye(2, dtype=torch.float64)
        ortho_err = float(torch.max(torch.abs(MtM - I)))
        det_val = float(torch.det(M))
        dets.append(round(det_val, 10))
        if ortho_err > 1e-10 or abs(abs(det_val) - 1.0) > 1e-10:
            all_orthogonal = False
    results["P3_pytorch_weyl_b2_elements_orthogonal"] = {
        "pass": all_orthogonal,
        "dets": dets,
        "reason": "All W(B2) elements are orthogonal: M^T M = I, det = ±1",
    }

    # ------------------------------------------------------------------
    # P4 (pytorch): Each Weyl element permutes the 8-root set
    # ------------------------------------------------------------------
    all_permute = True
    for name, M in elements:
        for root in ALL_ROOTS_B2:
            img = M @ root
            if not root_in_set_b2(img, ALL_ROOTS_B2_SET):
                all_permute = False
                break
    results["P4_pytorch_weyl_b2_permutes_roots"] = {
        "pass": all_permute,
        "reason": "Every W(B2) element maps each B2 root to another B2 root",
    }

    # ------------------------------------------------------------------
    # P5 (pytorch): Long roots have length sqrt(2), short roots length 1
    # ------------------------------------------------------------------
    long_roots = [ROOT_e1pe2, ROOT_me1me2, ROOT_e1me2, ROOT_me1pe2]
    short_roots = [ROOT_e1, ROOT_me1, ROOT_e2, ROOT_me2]
    long_lengths_ok = all(
        abs(float(torch.linalg.norm(r)) - SQRT2) < 1e-10 for r in long_roots
    )
    short_lengths_ok = all(
        abs(float(torch.linalg.norm(r)) - 1.0) < 1e-10 for r in short_roots
    )
    ratio = SQRT2 / 1.0
    results["P5_pytorch_b2_root_lengths_sqrt2_and_1"] = {
        "pass": long_lengths_ok and short_lengths_ok,
        "long_roots_ok": long_lengths_ok,
        "short_roots_ok": short_lengths_ok,
        "ratio": ratio,
        "reason": "B2: long roots length=sqrt(2), short roots length=1; ratio = sqrt(2) characteristic of B2",
    }

    # ------------------------------------------------------------------
    # P6 (pytorch): Coxeter relation (s1*s2)^4 = I
    # ------------------------------------------------------------------
    prod4 = S1 @ S2 @ S1 @ S2 @ S1 @ S2 @ S1 @ S2
    I = torch.eye(2, dtype=torch.float64)
    coxeter_err = float(torch.max(torch.abs(prod4 - I)))
    results["P6_pytorch_coxeter_relation_s1s2_order_4"] = {
        "pass": coxeter_err < 1e-10,
        "max_err": coxeter_err,
        "reason": "(s1*s2)^4 = I for B2; Coxeter order of the pair (s1,s2) is 4 (unlike 3 for A2)",
    }

    # ------------------------------------------------------------------
    # P7 (sympy): Cartan matrix B2 = [[2,-1],[-2,2]]
    # alpha1=(1,-1) long, alpha2=(0,1) short
    # A_{11} = 2; A_{12} = 2<alpha1,alpha2>/<alpha2,alpha2> = 2*(-1)/1 = -2
    # A_{21} = 2<alpha2,alpha1>/<alpha1,alpha1> = 2*(-1)/2 = -1; A_{22} = 2
    # ------------------------------------------------------------------
    alpha1_sym = sp.Matrix([1, -1])
    alpha2_sym = sp.Matrix([0, 1])
    A11 = 2 * alpha1_sym.dot(alpha1_sym) // alpha1_sym.dot(alpha1_sym)  # 2
    A12 = sp.Rational(2) * alpha1_sym.dot(alpha2_sym) / alpha2_sym.dot(alpha2_sym)
    A21 = sp.Rational(2) * alpha2_sym.dot(alpha1_sym) / alpha1_sym.dot(alpha1_sym)
    A22 = 2 * alpha2_sym.dot(alpha2_sym) // alpha2_sym.dot(alpha2_sym)  # 2
    cartan = sp.Matrix([[A11, A12], [A21, A22]])
    expected_cartan = sp.Matrix([[2, -2], [-1, 2]])
    # NOTE: Cartan convention depends on simple root ordering.
    # With alpha1=long=(1,-1), alpha2=short=(0,1):
    # A_{12} = 2<alpha1,alpha2>/<alpha2,alpha2> = 2*(-1)/1 = -2
    # A_{21} = 2<alpha2,alpha1>/<alpha1,alpha1> = 2*(-1)/2 = -1
    # So Cartan = [[2,-2],[-1,2]]. The conventional B2 is [[2,-1],[-2,2]] with alpha1=short.
    # With alpha1=short=(1,0), alpha2=long:
    alpha1_conv = sp.Matrix([1, 0])   # short
    alpha2_conv = sp.Matrix([-1, 1])  # (long = e1-e2 equiv; use (0,1)+(-1,0)=(-1,1))
    # Actually for standard B2: alpha1=e1=(1,0) short, alpha2=e2-e1=(-1,1) long, or
    # use the conventional: alpha1=(1,0), alpha2=(-1,1): <alpha1,alpha2>=-1, <alpha2,alpha2>=2
    # A_{12}=2*(-1)/2=-1; A_{21}=2*(-1)/1=-2 => [[2,-1],[-2,2]] as stated
    A11c = sp.Integer(2)
    A12c = sp.Rational(2) * alpha1_conv.dot(alpha2_conv) / alpha2_conv.dot(alpha2_conv)
    A21c = sp.Rational(2) * alpha2_conv.dot(alpha1_conv) / alpha1_conv.dot(alpha1_conv)
    A22c = sp.Integer(2)
    cartan_conv = sp.Matrix([[A11c, A12c], [A21c, A22c]])
    expected_conv = sp.Matrix([[2, -1], [-2, 2]])
    cartan_conv_correct = (sp.simplify(cartan_conv - expected_conv) == sp.zeros(2, 2))
    results["P7_sympy_cartan_matrix_b2"] = {
        "pass": bool(cartan_conv_correct),
        "cartan": str(cartan_conv),
        "expected": "[[2,-1],[-2,2]]",
        "reason": "B2 Cartan matrix = [[2,-1],[-2,2]] (asymmetric: encodes short/long root distinction)",
    }

    # ------------------------------------------------------------------
    # P8 (sympy): (s1*s2)^4 = I symbolically for B2
    # ------------------------------------------------------------------
    alpha1_s = sp.Matrix([1, 0])   # short
    alpha2_s = sp.Matrix([-1, 1])  # long (length sqrt(2))
    S1_sym = sp.eye(2) - sp.Rational(2) * alpha1_s * alpha1_s.T / alpha1_s.dot(alpha1_s)
    S2_sym = sp.eye(2) - sp.Rational(2) * alpha2_s * alpha2_s.T / alpha2_s.dot(alpha2_s)
    prod4_sym = sp.simplify((S1_sym * S2_sym) ** 4)
    coxeter_sym = (prod4_sym == sp.eye(2))
    results["P8_sympy_coxeter_s1s2_order_4_symbolic"] = {
        "pass": bool(coxeter_sym),
        "prod4": str(prod4_sym),
        "reason": "(s1*s2)^4 = I for B2 verified symbolically with exact rational arithmetic",
    }

    # ------------------------------------------------------------------
    # P9 (clifford): Clifford reflections map B2 roots to B2 roots
    # s_alpha(v) = -alpha * v * alpha^{-1} in Cl(2,0)
    # ------------------------------------------------------------------
    layout, blades = Cl(2)
    e1_cl = blades["e1"]
    e2_cl = blades["e2"]

    def to_cl(v_torch):
        return float(v_torch[0]) * e1_cl + float(v_torch[1]) * e2_cl

    def cl_reflection_correct(alpha_cl, v_cl):
        # For non-unit alpha: alpha^{-1} = ~alpha / |alpha|^2 (scalar part of alpha*~alpha)
        alpha_sq = float((alpha_cl * ~alpha_cl)[()])
        alpha_inv = ~alpha_cl / alpha_sq
        return -(alpha_cl * v_cl * alpha_inv)

    def cl_grade1(mv):
        # Extract grade-1 components: <mv, e_i> = (mv*e_i + e_i*mv)/2 (scalar part)
        e1_coeff = float((mv * e1_cl + e1_cl * mv)[()] ) / 2.0
        e2_coeff = float((mv * e2_cl + e2_cl * mv)[()] ) / 2.0
        return e1_coeff, e2_coeff

    def vec_in_b2_roots(vx, vy, tol=1e-8):
        for rt in ALL_ROOTS_B2:
            if abs(float(rt[0]) - vx) < tol and abs(float(rt[1]) - vy) < tol:
                return True
        return False

    alpha1_cl = to_cl(ALPHA1_B2)  # (1,-1), non-unit: |alpha1|^2 = 2
    alpha2_cl = to_cl(ALPHA2_B2)  # (0,1),  unit: |alpha2|^2 = 1

    all_cl_map_ok = True
    for root_t in ALL_ROOTS_B2:
        v_cl = to_cl(root_t)
        r1 = cl_reflection_correct(alpha1_cl, v_cl)
        r2 = cl_reflection_correct(alpha2_cl, v_cl)
        r1x, r1y = cl_grade1(r1)
        r2x, r2y = cl_grade1(r2)
        if not vec_in_b2_roots(r1x, r1y):
            all_cl_map_ok = False
        if not vec_in_b2_roots(r2x, r2y):
            all_cl_map_ok = False

    results["P9_clifford_reflections_map_b2_roots_to_roots"] = {
        "pass": all_cl_map_ok,
        "reason": (
            "Clifford reflection -alpha*v*alpha^{-1} (with alpha^{-1}=~alpha/|alpha|^2 "
            "for non-unit roots) maps each B2 root to another B2 root"
        ),
    }

    return results


