#
# G2 Cartan matrix: [[2,-1],[-3,2]]
#   alpha1 = simple short root
#   alpha2 = simple long root
#
# From Cartan matrix [[2,-1],[-3,2]]:
#   <alpha1^v, alpha2> = A_{12} = -1  (= 2<alpha1,alpha2>/<alpha1,alpha1>)
#   <alpha2^v, alpha1> = A_{21} = -3  (= 2<alpha2,alpha1>/<alpha2,alpha2>)
#
# Choose alpha1 = (1, 0) [short, length 1]
# Then alpha2 = (x, y) satisfying:
#   <alpha2,alpha1> = x = A_{21} * <alpha1,alpha1> / 2 = -3 * 1 / 2 = -3/2
#   <alpha1,alpha2> = x = A_{12} * <alpha2,alpha2> / 2
#   |alpha2|^2 = x^2 + y^2
#   From A_{12} = 2*(-3/2) / |alpha2|^2 = -1  =>  |alpha2|^2 = 3
#   So x = -3/2, y^2 = 3 - 9/4 = 3/4, y = sqrt(3)/2
#
# alpha1 = (1, 0)   [short, |alpha1| = 1]
# alpha2 = (-3/2, sqrt(3)/2)  [long, |alpha2| = sqrt(3)]
#
# All 12 G2 roots: 6 short + 6 long
# Short roots (A2 root system, length 1):
#   +-(1,0), +-(−1/2, sqrt(3)/2), +-(1/2, sqrt(3)/2)
# Long roots (length sqrt(3)):
#   +-(3/2, sqrt(3)/2), +-(0, sqrt(3)), +-(−3/2, sqrt(3)/2) [wait - need to re-derive]
#
# Actually the G2 roots from the simple roots (alpha1, alpha2) are generated by
# all reflections. Let me compute the full root system systematically.

SQRT3 = math.sqrt(3.0)
SQRT3_2 = SQRT3 / 2.0

# Simple roots for G2
G2_ALPHA1 = torch.tensor([1.0, 0.0], dtype=torch.float64)          # short
G2_ALPHA2 = torch.tensor([-3.0 / 2.0, SQRT3_2], dtype=torch.float64)  # long


def simple_reflection_matrix(alpha: torch.Tensor) -> torch.Tensor:
    """2x2 Weyl reflection matrix."""
    denom = torch.dot(alpha, alpha)
    return torch.eye(2, dtype=torch.float64) - 2.0 * torch.outer(alpha, alpha) / denom


# Build all 12 G2 roots by applying reflections
def build_g2_roots():
    """
    Build all 12 G2 roots by applying the Weyl group generators to the simple roots.
    The roots are the orbits of alpha1 (short) and alpha2 (long) under W(G2).
    Since W(G2) acts on alpha1 to give all 6 short roots, and on alpha2 to give
    all 6 long roots, we generate them via iterated reflections.
    """
    S1 = simple_reflection_matrix(G2_ALPHA1)
    S2 = simple_reflection_matrix(G2_ALPHA2)

    def reflect_root(root, S):
        return S @ root

    # Generate short roots: start from alpha1, apply all W(G2) elements
    # Short roots should be: +-alpha1, +-s2(alpha1), +-s1(s2(alpha1))
    # etc. — they form a hexagon of radius 1
    # alpha1 = (1,0)
    # s2(alpha1): alpha1 - 2*<alpha1,alpha2>/<alpha2,alpha2>*alpha2
    # <alpha1,alpha2> = 1*(-3/2) + 0*(sqrt(3)/2) = -3/2
    # <alpha2,alpha2> = 9/4 + 3/4 = 3
    # s2(alpha1) = (1,0) - 2*(-3/2)/3 * (-3/2, sqrt(3)/2) = (1,0) + 1 * (-3/2, sqrt(3)/2)
    #            = (-1/2, sqrt(3)/2)
    # s1(alpha2): alpha2 - 2*<alpha2,alpha1>/<alpha1,alpha1>*alpha1
    # <alpha2,alpha1> = -3/2; <alpha1,alpha1> = 1
    # s1(alpha2) = (-3/2, sqrt(3)/2) - 2*(-3/2)/1 * (1,0) = (-3/2, sqrt(3)/2) + (3,0) = (3/2, sqrt(3)/2)

    # Build 12 roots by closed orbit generation
    roots = set()
    queue = [G2_ALPHA1, G2_ALPHA2, -G2_ALPHA1, -G2_ALPHA2]
    for r in queue:
        roots.add(tuple(round(x, 10) for x in r.tolist()))

    # Apply reflections until closed
    for _ in range(20):
        new_roots = set()
        for r_tuple in roots:
            r = torch.tensor(list(r_tuple), dtype=torch.float64)
            for S in [S1, S2]:
                img = S @ r
                img_t = tuple(round(x, 10) for x in img.tolist())
                new_roots.add(img_t)
        roots.update(new_roots)
        if len(roots) == 12:
            break

    return sorted(roots), S1, S2


def root_in_set(v: torch.Tensor, root_set: list, tol: float = 1e-7) -> bool:
    """Check if v matches any root in root_set (list of tuples)."""
    vt = tuple(round(x, 10) for x in v.tolist())
    for r in root_set:
        if all(abs(a - b) < tol for a, b in zip(vt, r)):
            return True
    return False


def generate_weyl_group_g2(S1, S2):
    """
    Generate all 12 elements of W(G2) = Dih6.
    The group is generated by s1, s2 with (s1*s2)^6 = I.
    Elements: {(s1*s2)^k, s1*(s1*s2)^k for k=0..5}
    """
    I = torch.eye(2, dtype=torch.float64)
    R = S1 @ S2  # rotation element; has order 6 for G2
    elements = []
    Rk = I.clone()
    for k in range(6):
        elements.append((f"R{k}", Rk.clone()))
        elements.append((f"s1R{k}", S1 @ Rk))
        Rk = R @ Rk

    # Deduplicate
    unique = []
    for name, M in elements:
        is_dup = any(float(torch.max(torch.abs(M - E))) < 1e-8 for _, E in unique)
        if not is_dup:
            unique.append((name, M))

    return unique


