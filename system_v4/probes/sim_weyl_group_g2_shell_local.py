def run_boundary_tests():
    results = {}
    g2_roots, S1, S2 = build_g2_roots()
    weyl_elems = generate_weyl_group_g2(S1, S2)
    g2_roots_tensors = [torch.tensor(list(r), dtype=torch.float64) for r in g2_roots]
    short_roots = [r for r in g2_roots_tensors if abs(float(torch.dot(r, r)) - 1.0) < 1e-6]
    long_roots = [r for r in g2_roots_tensors if abs(float(torch.dot(r, r)) - 3.0) < 1e-6]

    # ------------------------------------------------------------------
    # B1 (pytorch): Longest element w0 maps ALL positive roots to negative roots
    # Positive roots: alpha1, s2(alpha1)=(-1/2,sqrt(3)/2), s2s1(alpha1)=(1/2,sqrt(3)/2)
    #                 alpha2, s1(alpha2)=(3/2,sqrt(3)/2), s1s2(alpha1+alpha2-type)
    # In 2D: positive roots have y > 0, or (y=0 and x>0)
    # ------------------------------------------------------------------
    I = torch.eye(2, dtype=torch.float64)
    R = S1 @ S2
    # w0 for G2: maps all positive roots to negative
    # w0 = -I for G2 (the longest element of all classical Lie algebras with -1 in center)
    # Verify: apply each Weyl element to positive roots to find the one that negates all
    positive_roots = [r for r in g2_roots_tensors
                      if float(r[1]) > 1e-8 or (abs(float(r[1])) < 1e-8 and float(r[0]) > 0)]
    negative_roots = [-r for r in positive_roots]

    # Find w0: the element that maps all positive roots to negative
    w0 = None
    for _, M in weyl_elems:
        if all(
            any(float(torch.max(torch.abs(M @ pr - nr))) < 1e-6 for nr in negative_roots)
            for pr in positive_roots
        ):
            w0 = M
            break

    w0_found = w0 is not None
    # Check if w0 = -I
    neg_I = -torch.eye(2, dtype=torch.float64)
    w0_is_neg_I = w0 is not None and float(torch.max(torch.abs(w0 - neg_I))) < 1e-6
    results["B1_pytorch_longest_element_negates_all_positive_roots"] = {
        "pass": w0_found,
        "w0_found": w0_found,
        "w0_is_neg_I": w0_is_neg_I,
        "num_positive_roots": len(positive_roots),
        "reason": "Longest element w0 maps all 6 positive G2 roots to their negatives",
    }

    # ------------------------------------------------------------------
    # B2 (pytorch): w0^2 = I (longest element has order 2)
    # ------------------------------------------------------------------
    if w0 is not None:
        w0_sq = w0 @ w0
        w0_sq_is_I = float(torch.max(torch.abs(w0_sq - I))) < 1e-8
    else:
        w0_sq_is_I = False
    results["B2_pytorch_longest_element_order_2"] = {
        "pass": w0_sq_is_I,
        "reason": "w0^2 = I: the longest element is an involution (order 2) in W(G2)",
    }

    # ------------------------------------------------------------------
    # B3 (pytorch): Every W(G2) element is orthogonal (det = +-1, M^T M = I)
    # ------------------------------------------------------------------
    all_orthogonal = True
    for _, M in weyl_elems:
        MtM = M.T @ M
        ortho_err = float(torch.max(torch.abs(MtM - I)))
        det_val = float(torch.det(M))
        if ortho_err > 1e-8 or abs(abs(det_val) - 1.0) > 1e-8:
            all_orthogonal = False
    results["B3_pytorch_all_weyl_elements_orthogonal"] = {
        "pass": all_orthogonal,
        "reason": "All 12 W(G2) elements are orthogonal matrices: M^T M = I, det = +-1",
    }

    # ------------------------------------------------------------------
    # B4 (sympy): (s1*s2)^6 = I AND (s1*s2)^3 != I — G2 Coxeter order is EXACTLY 6
    # ------------------------------------------------------------------
    s_sqrt3 = sp.sqrt(3)
    a1_sym = sp.Matrix([1, 0])
    a2_sym = sp.Matrix([-sp.Rational(3, 2), s_sqrt3 / 2])
    S1_sym = sp.eye(2) - 2 * a1_sym * a1_sym.T / a1_sym.dot(a1_sym)
    S2_sym = sp.eye(2) - 2 * a2_sym * a2_sym.T / a2_sym.dot(a2_sym)
    R_sym = S1_sym * S2_sym
    R3_sym = sp.simplify(R_sym ** 3)
    R6_sym = sp.simplify(R_sym ** 6)
    order_exactly_6 = (R6_sym == sp.eye(2)) and (R3_sym != sp.eye(2))
    results["B4_sympy_coxeter_order_exactly_6"] = {
        "pass": bool(order_exactly_6),
        "R3_is_identity": str(R3_sym == sp.eye(2)),
        "R6_is_identity": str(R6_sym == sp.eye(2)),
        "reason": "(s1*s2)^6 = I AND (s1*s2)^3 != I: Coxeter order is exactly 6 for G2",
    }

    # ------------------------------------------------------------------
    # B5 (z3): SAT — the G2 Cartan entry A_{21} = -3 is consistent
    # ------------------------------------------------------------------
    s = Solver()
    A21_z3 = Int("A21")
    s.add(A21_z3 == -3)
    sat_result = s.check()
    results["B5_z3_sat_g2_cartan_a21_minus3"] = {
        "pass": sat_result == sat,
        "z3_result": str(sat_result),
        "reason": "SAT: G2 Cartan entry A_{21}=-3 is consistent; encodes sqrt(3) length ratio",
    }

    # ------------------------------------------------------------------
    # B6 (clifford): W(G2) elements permute ALL 12 roots (closure under group action)
    # ------------------------------------------------------------------
    layout, blades = Cl(2)
    e1 = blades["e1"]
    e2 = blades["e2"]

    def cl_reflect_gen(alpha_t, v_cl):
        ax, ay = float(alpha_t[0]), float(alpha_t[1])
        alpha_cl = ax * e1 + ay * e2
        return -(alpha_cl * v_cl * ~alpha_cl)

    def cl_grade1(mv):
        e1c = float((mv * ~e1)[()] / float((e1 * ~e1)[()]))
        e2c = float((mv * ~e2)[()] / float((e2 * ~e2)[()]))
        return e1c, e2c

    # Test: apply s1 (unit short root) to each G2 root and check it stays in G2
    all_close_under_s1 = True
    for r_t in g2_roots_tensors:
        v_cl = float(r_t[0]) * e1 + float(r_t[1]) * e2
        r_cl = cl_reflect_gen(G2_ALPHA1, v_cl)
        rx_val, ry_val = cl_grade1(r_cl)
        img = torch.tensor([rx_val, ry_val], dtype=torch.float64)
        if not root_in_set(img, g2_roots):
            all_close_under_s1 = False

    results["B6_clifford_g2_roots_closed_under_s1_reflection"] = {
        "pass": all_close_under_s1,
        "reason": "All 12 G2 roots map to G2 roots under Clifford reflection by s1: root system is closed",
    }

    # ------------------------------------------------------------------
    # B7 (rustworkx): W(G2) Cayley graph has 12 nodes; W(A2) subgraph has 6 nodes
    # The A2 subgraph is embedded in the G2 Cayley graph
    # ------------------------------------------------------------------
    g_g2 = rx.PyDiGraph()
    elem_names = [name for name, _ in weyl_elems]
    node_indices = {name: g_g2.add_node(name) for name in elem_names}
    elem_mats = {name: M for name, M in weyl_elems}

    def mat_to_name(M, em, tol=1e-6):
        for n, E in em.items():
            if float(torch.max(torch.abs(M - E))) < tol:
                return n
        return None

    for name in elem_names:
        w = elem_mats[name]
        for gen, label in [(S1, "s1"), (S2, "s2")]:
            img = mat_to_name(gen @ w, elem_mats)
            if img:
                g_g2.add_edge(node_indices[name], node_indices[img], label)

    # A2 subgroup elements: those whose matrix is in W(A2) = S3
    ALPHA1_a2 = torch.tensor([1.0, 0.0], dtype=torch.float64)
    ALPHA2_a2 = torch.tensor([-0.5, SQRT3_2], dtype=torch.float64)
    S1_a2 = simple_reflection_matrix(ALPHA1_a2)
    S2_a2 = simple_reflection_matrix(ALPHA2_a2)
    a2_mats = [
        torch.eye(2, dtype=torch.float64), S1_a2, S2_a2,
        S1_a2 @ S2_a2, S2_a2 @ S1_a2, S1_a2 @ S2_a2 @ S1_a2,
    ]
    a2_node_names = [
        name for name, M in weyl_elems
        if any(float(torch.max(torch.abs(M - Ma))) < 1e-6 for Ma in a2_mats)
    ]

    results["B7_rustworkx_a2_subgraph_in_g2_cayley"] = {
        "pass": g_g2.num_nodes() == 12 and len(a2_node_names) == 6,
        "g2_cayley_nodes": g_g2.num_nodes(),
        "a2_subgraph_nodes": len(a2_node_names),
        "reason": "G2 Cayley graph: 12 nodes; A2 subgroup = 6 nodes embedded as subgraph",
    }

    # ------------------------------------------------------------------
    # B8 (xgi): At the boundary between G2 and A2: short root hyperedge connects
    # the A2 sub-root-system (6 short roots) and the G2 full root system (12 roots)
    # ------------------------------------------------------------------
    H = xgi.Hypergraph()
    H.add_nodes_from(["short_roots", "long_roots", "A2_subrootsystem", "G2_rootsystem",
                      "s1_generator", "s2_generator"])
    # Boundary: A2 is contained in G2 via short roots
    H.add_edge(["short_roots", "A2_subrootsystem"])
    H.add_edge(["short_roots", "long_roots", "G2_rootsystem"])
    H.add_edge(["s1_generator", "s2_generator", "G2_rootsystem"])
    results["B8_xgi_a2_g2_boundary_hyperedges"] = {
        "pass": H.num_edges == 3 and "short_roots" in H.nodes,
        "num_edges": H.num_edges,
        "num_nodes": H.num_nodes,
        "reason": "XGI: boundary hyperedges for A2 in G2 nesting; short/long root split; generator coupling",
    }

    return results


